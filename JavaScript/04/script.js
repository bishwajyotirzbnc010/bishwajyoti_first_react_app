function showMonth(month) {
    if(month === 1){
        alert("JANUARY");
    } else if(month === 2){
        alert("FEBRUARY");
    } else if(month === 3){
        alert("MARCH");
    } else if(month === 4){
        alert("APRIL");
    } else if(month === 5){
        alert("MAY");
    } else if(month === 6){
        alert("JUNE");
    } else if(month === 7){
        alert("JULY");
    } else if(month === 8){
        alert("AUGUST");
    } else if(month === 9){
        alert("SEPTEMBER");
    } else if(month === 10){
        alert("OCTOBER");
    } else if(month === 11){
        alert("NOVEMBER");
    } else if(month === 12){
        alert("DECEMBER");
    } else{
        alert("Invalid Entry");
    }
}
