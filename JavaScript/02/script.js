
function bothpluscube() {
    let a = 2;
    let b = 4;
    let calc = ((a + b) ** 3) - (3 * a * b * (a + b));
    alert("Result: " + calc);
}

function bothminuscube() {
    let a = 6;
    let b = 4;
    let calc = ((a - b) ** 3) + (3 * a * b * (a - b));
    alert("Result: " + calc);
}

function bothplusSquare() {
    let a = 8;
    let b = 4;
    let calc = ((a + b) ** 2) - (2 * a * b);
    alert("Result: " + calc);
}
